package JavaGFT.exercicios1;
    
public class Calculadora{

    public static void soma(double numero1, double numero2){
        double resultado = numero1 + numero2;
        System.out.println("A soma dos dois números é: " + resultado);
    }

    public static void subtraçao(double numero1, double numero2){
        double resultado = numero1 - numero2;
        System.out.println("A subtração dos dois números é: " + resultado);
    }

    public static void multiplicaçao(double numero1, double numero2){
        double resultado = numero1 * numero2;
        System.out.println("A multiplicação dos números é: " + resultado);
    }

    public static void divisao(double numero1, double numero2){
        double resultado = numero1 / numero2;
        System.out.println("A divisão dos números é: " + resultado);
    }
   
}
